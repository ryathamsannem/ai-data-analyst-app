export type BrandingConfig = {
  appName: string;
  shortName: string;
  supportEmail: string;
  primaryColor: string;
  secondaryColor: string;
  pdfFooterText: string;
  exportFilePrefix: string;
  logoUrl?: string;
  /** Optional sidebar badge, e.g. "V1". */
  version?: string;
};

