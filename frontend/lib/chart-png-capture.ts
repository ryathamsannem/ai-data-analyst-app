function resolveExportBackground(optionsBg?: string): string {
  if (optionsBg) return optionsBg;
  if (typeof document === "undefined") return "#ffffff";
  return document.documentElement.classList.contains("dark") ? "#0f172a" : "#ffffff";
}

function isUnsafeCssColor(value: string): boolean {
  return /color-mix\s*\(|color\s*\(/i.test(value);
}

function resolveCssColorForExport(value: string): string {
  const v = value.trim();
  if (!v || v === "none" || v === "transparent") return v;
  if (!isUnsafeCssColor(v)) return v;

  if (typeof document === "undefined") return "#334155";

  const probe = document.createElement("span");
  probe.style.cssText =
    "position:fixed;left:-9999px;top:0;visibility:hidden;pointer-events:none;";
  probe.style.color = v;
  document.body.appendChild(probe);
  const resolved = window.getComputedStyle(probe).color;
  probe.remove();
  if (resolved && !isUnsafeCssColor(resolved)) return resolved;
  return "#334155";
}

function resolvePlotRoot(container: HTMLElement): HTMLElement {
  const selectors = [
    ".overview-chart-plot-inner .recharts-responsive-container",
    ".overview-chart-plot-inner .recharts-wrapper",
    ".overview-chart-plot .recharts-responsive-container",
    ".charts-tab-viz-plot-stage .recharts-responsive-container",
    ".charts-tab-viz-plot-stage .recharts-wrapper",
    ".ai-insights-viz-plot .recharts-responsive-container",
    ".recharts-responsive-container",
    ".recharts-wrapper",
  ];
  for (const sel of selectors) {
    const el = container.querySelector(sel);
    if (el instanceof HTMLElement) return el;
  }
  const plotStage =
    container.querySelector(".charts-tab-viz-plot-stage") ??
    container.querySelector(".overview-chart-plot-inner");
  if (plotStage instanceof HTMLElement) return plotStage;
  return container;
}

function findPrimaryChartSvg(root: HTMLElement): SVGSVGElement | null {
  const svgs = [...root.querySelectorAll("svg")].filter(
    (s): s is SVGSVGElement => s instanceof SVGSVGElement
  );
  if (!svgs.length) return null;
  let best = svgs[0]!;
  let bestArea = 0;
  for (const svg of svgs) {
    const r = svg.getBoundingClientRect();
    const area = r.width * r.height;
    if (area > bestArea) {
      bestArea = area;
      best = svg;
    }
  }
  return bestArea > 4 ? best : svgs[0] ?? null;
}

function isSvgAxisTitleText(src: Element): boolean {
  if (src.classList.contains("recharts-label")) return true;
  const parent = src.parentElement;
  if (parent?.classList.contains("recharts-label")) return true;
  if (!parent?.closest(".recharts-xAxis, .recharts-yAxis")) return false;
  const fw = Number.parseInt(window.getComputedStyle(src).fontWeight, 10);
  return Number.isFinite(fw) && fw >= 600;
}

function resolveExportTextFill(
  src: Element,
  plotBackground: string,
  theme: ResolvedChartAxisTheme
): string {
  const cs = window.getComputedStyle(src);
  const col = cs.fill || cs.color;
  const themed = isSvgAxisTitleText(src) ? theme.label : theme.tick;
  const resolved = resolveCssColorForExport(col || themed);
  return ensureReadableExportTextFill(resolved, plotBackground, {
    lightText: theme.tick,
    darkText: theme.label,
  });
}

function cloneSvgWithInlineStyles(
  source: SVGSVGElement,
  plotBackground: string,
  theme: ResolvedChartAxisTheme
): SVGSVGElement {
  const clone = source.cloneNode(true) as SVGSVGElement;
  const tagSel = "path,rect,circle,line,text,tspan,polygon,polyline,g";
  const srcEls = source.querySelectorAll(tagSel);
  const cloneEls = clone.querySelectorAll(tagSel);
  cloneEls.forEach((el, i) => {
    const src = srcEls[i];
    if (!src) return;
    const cs = window.getComputedStyle(src);
    const fill = cs.fill;
    const stroke = cs.stroke;
    if (fill && fill !== "none" && !fill.includes("rgba(0, 0, 0, 0)")) {
      el.setAttribute("fill", resolveCssColorForExport(fill));
    }
    if (stroke && stroke !== "none") {
      el.setAttribute("stroke", resolveCssColorForExport(stroke));
    }
    const sw = cs.strokeWidth;
    if (sw && sw !== "0px") el.setAttribute("stroke-width", sw);
    if (el.tagName === "text" || el.tagName === "tspan") {
      el.setAttribute("fill", resolveExportTextFill(src, plotBackground, theme));
      const fs = cs.fontSize;
      if (fs) el.setAttribute("font-size", fs);
      const ff = cs.fontFamily;
      if (ff) el.setAttribute("font-family", ff);
      const transform = cs.transform;
      if (transform && transform !== "none") {
        el.setAttribute("transform", transform);
      }
    }
  });
  return clone;
}

function tightenSvgViewBox(
  clone: SVGSVGElement,
  fallbackWidth: number,
  fallbackHeight: number,
  opts?: { minPadPx?: number }
): { width: number; height: number } {
  const minPad = opts?.minPadPx ?? 8;
  const mount = document.createElement("div");
  mount.style.cssText =
    "position:fixed;left:-9999px;top:0;visibility:hidden;pointer-events:none;overflow:visible;";
  document.body.appendChild(mount);
  mount.appendChild(clone);
  let width = fallbackWidth;
  let height = fallbackHeight;
  try {
    const bbox = clone.getBBox();
    if (bbox.width > 12 && bbox.height > 12) {
      const textNodes = [...clone.querySelectorAll("text")];
      let maxTextBottom = bbox.y + bbox.height;
      for (const text of textNodes) {
        try {
          const tb = text.getBBox();
          maxTextBottom = Math.max(maxTextBottom, tb.y + tb.height);
        } catch {
          // ignore detached text metrics
        }
      }
      const padX = minPad;
      const padTop = minPad;
      const padBottom = textNodes.length >= 4 ? minPad + 10 : minPad + 4;
      const x = bbox.x - padX;
      const y = bbox.y - padTop;
      const w = bbox.width + padX * 2;
      const h = Math.max(bbox.height, maxTextBottom - bbox.y) + padTop + padBottom;
      clone.setAttribute("viewBox", `${x} ${y} ${w} ${h}`);
      width = Math.max(1, Math.round(w));
      height = Math.max(1, Math.round(h));
    }
  } catch {
    // keep fallback viewBox
  } finally {
    mount.remove();
  }
  return { width, height };
}

