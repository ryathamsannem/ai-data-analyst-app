type ExportLegendEntry = {
  label: string;
  color: string;
};

function extractRechartsLegendEntries(root: HTMLElement): ExportLegendEntry[] {
  const wrapper = root.querySelector(".recharts-legend-wrapper");
  if (!wrapper) return [];

  const entries: ExportLegendEntry[] = [];
  wrapper.querySelectorAll(".recharts-legend-item").forEach((item) => {
    const label =
      item.querySelector(".recharts-legend-item-text")?.textContent?.trim() ?? "";
    if (!label) return;

    const icon = item.querySelector("svg");
    const shape = icon?.querySelector("path, rect, circle");
    let color = "#6366f1";
    if (shape instanceof SVGElement) {
      color =
        shape.getAttribute("fill") ||
        window.getComputedStyle(shape).fill ||
        color;
    }
    entries.push({
      label,
      color: resolveCssColorForExport(color),
    });
  });
  return entries;
}

function renderLegendChromeToPng(
  entries: ExportLegendEntry[],
  exportWidth: number,
  scale: number,
  palette: ExportPalette
): { dataUrl: string; width: number; height: number } | null {
  if (!entries.length) return null;

  const fontFamily =
    'ui-sans-serif, system-ui, -apple-system, "Segoe UI", Roboto, sans-serif';
  const labelFont = `500 ${RADIAL_EXPORT_LEGEND_FONT_PX}px ${fontFamily}`;
  const innerW = exportWidth - COMPOSITE_PAD_X * 2;
  const rowH = RADIAL_EXPORT_LEGEND_FONT_PX + RADIAL_EXPORT_LEGEND_ROW_EXTRA_PX;
  const rowGap = RADIAL_EXPORT_LEGEND_ITEM_GAP_PX;
  const swatch = RADIAL_EXPORT_LEGEND_ICON_PX;
  const swatchGap = RADIAL_EXPORT_LEGEND_SWATCH_GAP_PX;

  const probe = document.createElement("canvas");
  const pctx = probe.getContext("2d");
  if (!pctx) return null;

  const itemWidths = entries.map((entry) => {
    pctx.font = labelFont;
    return swatch + swatchGap + pctx.measureText(entry.label).width + 16;
  });
  const totalOneRow =
    itemWidths.reduce((a, b) => a + b, 0) + rowGap * (entries.length - 1);

  const rows: ExportLegendEntry[][] = [];
  if (totalOneRow <= innerW) {
    rows.push(entries);
  } else {
    let current: ExportLegendEntry[] = [];
    let rowW = 0;
    entries.forEach((entry, i) => {
      const w = itemWidths[i] ?? 0;
      const gap = current.length > 0 ? rowGap : 0;
      if (current.length > 0 && rowW + gap + w > innerW) {
        rows.push(current);
        current = [entry];
        rowW = w;
      } else {
        current.push(entry);
        rowW += gap + w;
      }
    });
    if (current.length) rows.push(current);
  }

  const contentH =
    rows.length * rowH + Math.max(0, rows.length - 1) * rowGap + 14;

  const canvas = document.createElement("canvas");
  canvas.width = exportWidth * scale;
  canvas.height = contentH * scale;
  const ctx = canvas.getContext("2d");
  if (!ctx) return null;

  ctx.scale(scale, scale);
  ctx.fillStyle = palette.background;
  ctx.fillRect(0, 0, exportWidth, contentH);

  let y = 4;
  for (const row of rows) {
    const widths = row.map((entry) => {
      ctx.font = labelFont;
      return swatch + swatchGap + ctx.measureText(entry.label).width + 16;
    });
    const totalW =
      widths.reduce((a, b) => a + b, 0) + rowGap * (row.length - 1);
    let x = (exportWidth - totalW) / 2;

    row.forEach((entry, i) => {
      const w = widths[i] ?? 0;
      ctx.fillStyle = entry.color;
      ctx.beginPath();
      ctx.arc(x + swatch / 2, y + rowH / 2, swatch / 2, 0, Math.PI * 2);
      ctx.fill();

      ctx.font = labelFont;
      ctx.fillStyle = palette.chipValue;
      ctx.textAlign = "left";
      ctx.textBaseline = "middle";
      ctx.fillText(entry.label, x + swatch + swatchGap, y + rowH / 2);
      x += w + rowGap;
    });
    y += rowH + rowGap;
  }

  return {
    dataUrl: canvas.toDataURL("image/png"),
    width: exportWidth,
    height: contentH,
  };
}

/** Marks export roots so charts disable Recharts animation before capture. */
export const CHART_PNG_CAPTURE_ATTR = "data-chart-png-capture";

function nextFrame(): Promise<void> {
  return new Promise((resolve) => requestAnimationFrame(() => resolve()));
}

/** Wait until a line/area path stops changing (animation settled) or timeout. */
export async function waitForStableChartSvg(
  root: HTMLElement,
  maxMs = 900
): Promise<void> {
  if (typeof window === "undefined") return;

  const deadline = Date.now() + maxMs;
  let stableFrames = 0;
  let prevPath = "";

  while (Date.now() < deadline) {
    const curve = root.querySelector(
      ".recharts-line-curve, .recharts-area-curve"
    );
    if (!curve) {
      stableFrames = 0;
      await nextFrame();
      continue;
    }
    const d = curve.getAttribute("d") ?? "";
    if (d.length > 16 && d === prevPath) {
      stableFrames += 1;
      if (stableFrames >= 2) break;
    } else {
      stableFrames = 0;
      prevPath = d;
    }
    await nextFrame();
  }

  await new Promise<void>((resolve) => window.setTimeout(resolve, 120));
}

/** Reflow Recharts after layout changes (e.g. sidebar collapse) before PNG capture. */
export async function prepareChartForPngCapture(
  sourceRoot: HTMLElement
): Promise<void> {
  if (typeof window === "undefined") return;

  sourceRoot.setAttribute(CHART_PNG_CAPTURE_ATTR, "1");

  window.dispatchEvent(new Event("resize"));
  await nextFrame();
  await nextFrame();

  const plotRoot = resolvePlotRoot(sourceRoot);
  const rect = plotRoot.getBoundingClientRect();
  const width = Math.max(1, Math.floor(rect.width || plotRoot.clientWidth));
  const height = Math.max(1, Math.floor(rect.height || plotRoot.clientHeight));
  plotRoot.style.width = `${width}px`;
  if (height > 1) plotRoot.style.height = `${height}px`;

  await new Promise<void>((resolve) => window.setTimeout(resolve, 160));
  window.dispatchEvent(new Event("resize"));
  await nextFrame();
  await nextFrame();
  await waitForStableChartSvg(sourceRoot);
}

async function renderPlotSvgToPng(
  container: HTMLElement,
  scale: number,
  targetPlotWidth: number | undefined,
  plotBackground: string
): Promise<{ dataUrl: string; width: number; height: number } | null> {
  const plotRoot = resolvePlotRoot(container);
  const svg = findPrimaryChartSvg(plotRoot);
  if (!svg) return null;

  const rect = svg.getBoundingClientRect();
  let width = Math.max(1, Math.round(rect.width));
  let height = Math.max(1, Math.round(rect.height));
  if (width <= 2 || height <= 2) {
    const pr = plotRoot.getBoundingClientRect();
    width = Math.max(pr.width || plotRoot.clientWidth || 720, 1);
    height = Math.max(pr.height || plotRoot.clientHeight || 320, 1);
  }

  const plotBox = plotRoot.getBoundingClientRect();
  if (width < 320 && plotBox.width > width) {
    width = Math.max(width, Math.round(Math.min(plotBox.width, 960)));
    height = Math.max(height, Math.round(Math.min(plotBox.height, 540)));
  }

  const axisTheme = readResolvedChartAxisTheme(container);
  const clone = cloneSvgWithInlineStyles(svg, plotBackground, axisTheme);
  applyPngExportSvgPolish(clone, {
    darkBackground: isDarkExportBackground(plotBackground),
  });
  const radialExport = isRadialChartRoot(container);
  if (radialExport) {
    stripRadialLegendFromSvgClone(clone);
  }
  const tightened = tightenSvgViewBox(clone, width, height, {
    minPadPx: radialExport ? RADIAL_EXPORT_MIN_SVG_PAD_PX : 4,
  });
  width = tightened.width;
  height = tightened.height;

  if (targetPlotWidth && targetPlotWidth > width) {
    const boost = Math.min(targetPlotWidth / width, 1.22);
    width = Math.round(width * boost);
    height = Math.round(height * boost);
  }

  clone.setAttribute("width", String(width));
  clone.setAttribute("height", String(height));
  clone.setAttribute("xmlns", "http://www.w3.org/2000/svg");
  if (!clone.getAttribute("viewBox")) {
    clone.setAttribute("viewBox", `0 0 ${width} ${height}`);
  }
  clone.setAttribute("preserveAspectRatio", "xMidYMid meet");
  clone.setAttribute("overflow", "visible");

  const svgString = new XMLSerializer().serializeToString(clone);
  const canvas = document.createElement("canvas");
  canvas.width = width * scale;
  canvas.height = height * scale;
  const ctx = canvas.getContext("2d");
  if (!ctx) return null;
  ctx.fillStyle = plotBackground;
  ctx.fillRect(0, 0, canvas.width, canvas.height);
  ctx.setTransform(scale, 0, 0, scale, 0, 0);

  const v = await Canvg.fromString(ctx, svgString);
  await v.render();
  return {
    dataUrl: canvas.toDataURL("image/png"),
    width,
    height,
  };
}

type ExportChip = {
  label: string;
  value: string;
  mono?: boolean;
  emphasis?: "change" | "default";
};

type ExportReasonBlock = {
  label: string;
  body: string;
};

function parseReasonFromNote(note: Element | null): ExportReasonBlock | null {
  if (!note) return null;

  const spans = [...note.querySelectorAll(":scope > span")];
  let label = spans[0]?.textContent?.trim() ?? "";
  let body = spans[1]?.textContent?.trim() ?? "";

  if (!body) {
    const raw = note.textContent?.trim().replace(/\s+/g, " ") ?? "";
    if (!raw) return null;
    const prefix = raw.match(/^why this chart\s*(?:[·•:—-]\s*)?/i);
    if (prefix) {
      label = label || "Why this chart";
      body = raw.slice(prefix[0].length).trim();
    } else {
      body = raw;
    }
  }

  if (!label && body) label = "Why this chart";
  if (!body) return null;

  return { label, body };
}

